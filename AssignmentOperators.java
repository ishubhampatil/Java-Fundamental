public class AssignmentOperators {
    public static void main(String[] args) 
    { 
        int a=10;
        System.out.println(a+=10);  //  20                   a=a+10   similar  to a+=10
        System.out.println(a-=10);  //  10
        System.out.println(a*=10);  //  100
        System.out.println(a/=10);  //  10
        System.out.println(a%=10);  //  0
    }
}