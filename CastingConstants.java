public class CastingConstants {
    public static void main (String[]args) {
       //implicit Casting- 
        double price= 90.50;
        double finalprice= price+10;
        System.out.println(finalprice); 
//-------------------------------------------------------------------------------------
        //Explicit Casting
        int P=100;
     int FP=P+(int)18.50;
     System.out.println(FP);

//-------------------------------------------------------------------------------------
        //Constants- we declare PI=3.14. so, it can't change in whole code.
     final float PI=3.14F;

    }
      
     


    
        
    
}
