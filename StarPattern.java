public class StarPattern {public static void main(String[] args) {
    for( int i= 0; i<4; i++){
        for(int j=0; j<=i; j++){System.out.print("*");}
        System.out.println();
    }
    
}
    
}
/*           Pattern                                                                        
line 0  *   i=0 j=0 
line 1  **  i=1 j=0,j=1                                                        
line 2  *** i=2 j=0, j=1 j=2                                    
line 3  **** i=3 j=0 j=1 j=2 j=3     */          





